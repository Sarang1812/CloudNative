# operators

# addition
print(f"10 + 20 = {10 + 20}")

# subtraction
print(f"10 - 20 = {10 - 20}")

# multiplication
print(f"20 * 10 = {20 * 10}")

# true division (returns float)
print(f"20 / 9 = {20 / 9}")
print(f"20 / 7 = {20 / 7}")

# mathematical operators added by python

# floor division (returns int floor value)
print(f"20 // 9 = {20 // 9}")
print(f"20 // 7 = {20 // 7}")

# power operator
print(f"square of 5 = {5 ** 2}")
print(f"cube of 5 = {5 ** 3}")

# repeatition operator
print(" - * - " * 10)


# comparison operators

print(f"10 > 5 = {10 > 5}")
print(f"10 < 5 = {10 < 5}")

print(f"10 >= 5 = {10 >= 5}")
print(f"10 <= 5 = {10 <= 5}")

print(f"10 == 5 = {10 == 5}")
print(f"10 != 5 = {10 != 5}")


# logical operators

print(f"True and True = {True and True}")
print(f"True and False = {True and False}")
print(f"False and True = {False and True}")
print(f"False and False = {False and False}")

print(f"True or True = {True or True}")
print(f"True or False = {True or False}")
print(f"False or True = {False or True}")
print(f"False or False = {False or False}")
