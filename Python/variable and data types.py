# create a variable with name as num

# int
num = 100
print(f"num = {num}, type of num = {type(num)}")

num = '100'
print(f"num = {num}, type of num = {type(num)}")

# float
salary = 15.50
print(f"salary = {salary}, type of salary = {type(salary)}")

# string
first_name = 'SARANG'
print(f"first_name = {first_name}, type of first_name = {type(first_name)}")

# string
last_name = "RAOOL"
print(f"last_name = {last_name}, type of last_name = {type(last_name)}")

# multiline string
address = """home name,
sinhgad road,
pune,
maharshtra, 411001"""
print(f"address = {address}, type of address = {type(address)}")

# bool (True/False)
enable = False
print(f"can_vote = {enable}, type of can_vote = {type(enable)}")

